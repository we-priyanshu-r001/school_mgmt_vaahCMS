            $data['rows'] = config('vaahcms.per_page');

            $data['fillable']['columns'] = Student::getFillableColumns();
            $data['fillable']['except'] = Student::getUnFillableColumns();
            $data['empty_item'] = Student::getEmptyItem();
            $data['genders'] = Taxonomy::getTaxonomyByType('genders');
            $data['batches'] = Batch::all();

            $data['actions'] = [];

            $response['success'] = true;
            $response['data'] = $data;

        }catch (\Exception $e){
            $response = [];
            $response['success'] = false;
            if(env('APP_DEBUG')){
                $response['errors'][] = $e->getMessage();
                $response['hint'] = $e->getTrace();
            } else{
                $response['errors'][] = trans("vaahcms-general.something_went_wrong");
            }
        }

        return $response;
    }


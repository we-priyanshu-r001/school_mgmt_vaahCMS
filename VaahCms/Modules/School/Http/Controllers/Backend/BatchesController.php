    public function getAssets(Request $request)
    {

        try{

            $data = [];

            $data['permission'] = [];
            $data['rows'] = config('vaahcms.per_page');

            $data['fillable']['columns'] = Batch::getFillableColumns();
            $data['fillable']['except'] = Batch::getUnFillableColumns();
            $data['empty_item'] = Batch::getEmptyItem();

            $data['total_students'] = Student::all()->count();
            $data['total_teachers'] = Teacher::all()->count();
            $data['actions'] = [];


            $response['success'] = true;
            $response['data'] = $data;

        }catch (\Exception $e){
            $response = [];
            $response['success'] = false;
            if(env('APP_DEBUG')){
                $response['errors'][] = $e->getMessage();
                $response['hint'] = $e->getTrace();
            } else{
                $response['errors'][] = trans("vaahcms-general.something_went_wrong");
            }
        }

        return $response;
    }

